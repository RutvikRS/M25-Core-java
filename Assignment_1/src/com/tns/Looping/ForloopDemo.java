package com.tns.Looping;

import java.util.Scanner;

public class ForloopDemo {

	public static void main(String[] args) {
		 Scanner s=new Scanner(System.in);
	     System.out.println("Enter THe Value Of n");
	     int n=s.nextInt();
	     for(int i=0;i<=n;i++)
	     {
	    	 System.out.println(i+""); 
	    	 s.close();
	     }

	}

}
